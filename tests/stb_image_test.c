#include <stdio.h>

#include "stb_image.h"

static const unsigned char png_1x1_rgba[] = {
    0x89, 0x50, 0x4e, 0x47, 0x0d, 0x0a, 0x1a, 0x0a,
    0x00, 0x00, 0x00, 0x0d, 0x49, 0x48, 0x44, 0x52,
    0x00, 0x00, 0x00, 0x01, 0x00, 0x00, 0x00, 0x01,
    0x08, 0x06, 0x00, 0x00, 0x00, 0x1f, 0x15, 0xc4,
    0x89, 0x00, 0x00, 0x00, 0x0d, 0x49, 0x44, 0x41,
    0x54, 0x08, 0xd7, 0x63, 0xf8, 0xcf, 0xc0, 0xf0,
    0x1f, 0x00, 0x05, 0x00, 0x01, 0xff, 0x89, 0x99,
    0x3d, 0x1d, 0x00, 0x00, 0x00, 0x00, 0x49, 0x45,
    0x4e, 0x44, 0xae, 0x42, 0x60, 0x82
};

int main(void) {
    int width = 0;
    int height = 0;
    int source_channels = 0;
    unsigned char *pixels = stbi_load_from_memory(
        png_1x1_rgba,
        (int)sizeof(png_1x1_rgba),
        &width,
        &height,
        &source_channels,
        STBI_rgb_alpha
    );

    if (pixels == NULL) {
        fprintf(stderr, "stb_image failed to decode PNG: %s\n", stbi_failure_reason());
        return 1;
    }

    const int valid = width == 1 && height == 1 && source_channels == 4 &&
                      pixels[0] == 255 && pixels[1] == 0 &&
                      pixels[2] == 0 && pixels[3] == 255;
    stbi_image_free(pixels);

    if (!valid) {
        fprintf(stderr, "decoded PNG did not contain the expected RGBA pixel\n");
        return 1;
    }

    return 0;
}
